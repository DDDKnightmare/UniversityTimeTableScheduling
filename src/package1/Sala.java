package package1;

public class Sala {
    
    public int codigo;
    public String descricao;
    public int tipoDeSala;
    public int capacidade;

    public Sala(int codigo, String descricao, int tipoDeSala, int capacidade) {
        this.codigo = codigo;
        this.descricao = descricao;
        this.tipoDeSala = tipoDeSala;
        this.capacidade = capacidade;
    }
}
