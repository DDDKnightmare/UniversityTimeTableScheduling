package package1;

public class Curso {
    
    public int codigo;
    public String nome;
    public int numeroPeriodos;
    public int turno;

    public Curso(int codigo, String nome, int numeroPeriodos, int turno) {
        this.codigo = codigo;
        this.nome = nome;
        this.numeroPeriodos = numeroPeriodos;
        this.turno = turno;
    }
}
